def main():
    data = json.loads(TARGET.read_text(encoding="utf-8"))
    touched = []
    missing = set(NEW_DESCRIBES.keys())
    for item in data:
        iid = item.get("id")
        if iid in NEW_DESCRIBES:
            old = item.get("Describe", [""])[0] if item.get("Describe") else ""
            item["Describe"] = [NEW_DESCRIBES[iid]]
            touched.append((iid, old[:30], NEW_DESCRIBES[iid][:30]))
            missing.discard(iid)

    TARGET.write_text(
        json.dumps(data, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print(f"updated: {len(touched)} items")
    for iid, old, new in touched:
        print(f"  {iid}  {old}... -> {new}...")
    if missing:
        print(f"WARNING missing ids: {sorted(missing)}")


if __name__ == "__main__":
    main()
