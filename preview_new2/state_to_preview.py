def fix_json(text):
    """状态机修复非标准JSON（与index.html fixJson一致）:
    1. 字符串内字面换行符 -> \\n
    2. 未转义反斜杠 -> \\\\
    3. 字符串内裸双引号 -> \\"
    """
    out = []
    in_str = False
    i = 0
    n = len(text)
    while i < n:
        c = text[i]
        if not in_str:
            out.append(c)
            if c == '"':
                in_str = True
            i += 1
            continue
        # 以下在字符串内部
        if c == '\\':
            nx = text[i + 1] if i + 1 < n else ''
            if nx in '"\\\/bfnrt':
                out.append(c + nx)
                i += 2
            elif nx == 'u' and i + 5 < n and all(ch in '0123456789abcdefABCDEF' for ch in text[i+2:i+6]):
                out.append(text[i:i+6])
                i += 6
            else:
                out.append('\\\\')
                i += 1
        elif c == '"':
            nx = text[i + 1] if i + 1 < n else ''
            if nx == '' or nx in ',:]}':
                out.append(c)
                in_str = False
                i += 1
            elif nx in ' \t\r\n':
                # 引号后是空白/换行 -> 向前看跳过空白
                j = i + 1
                while j < n and text[j] in ' \t\r\n':
                    j += 1
                after = text[j] if j < n else ''
                if after == '' or after in ',:]}':
                    out.append(c)
                    in_str = False
                    i += 1
                else:
                    out.append('\\"')
                    i += 1
            else:
                out.append('\\"')
                i += 1
        elif c == '\n':
            out.append('\\n')
            i += 1
        elif c == '\r':
            i += 1
        else:
            out.append(c)
            i += 1
    return ''.join(out)


